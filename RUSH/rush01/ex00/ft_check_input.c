int	ft_check_input(char *text)
{
	int	i;

	i = 0;
	while (text[i])
	{
		if (i % 2 == 0 && (text[i] < '1' || text[i] > '4'))
			return (0);
		if (i % 2 == 1 && text[i] != ' ')
			return (0);
		i++;
	}
	if (i == 16 * 2 - 1)
		return (1);
	return (0);
}
